
public class HocSinh {
	private String ten;
	private String lop;
	private int tuoi;
	
	public void setName(String name) {
		this.ten = name;
	}
	public void setTuoi(int tuoi) {
		this.tuoi = tuoi;
	}
	public void setLop(String lop) {
		this.lop = lop;
	}
	public String getName() {
		return this.ten;
	}
	public String getLop() {
		return this.lop;
	}
	public int getTuoi() {
		return this.tuoi;
	}
	

}
